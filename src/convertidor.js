function convertir(num) {
    if(num == 1)
    {
        return "I";
    }
    if(num == 2)
    {
        return "II";
    }
    if(num == 5)
    {
       return "V";
    }
    if(num == 10)
    {
       return "X";
    }
    if(num == 50)
    {
       return "L";
    }
    if(num == 100)
    {
       return "C";
    }
  }
  
  export default convertir;
  